		RET
	UPDATE_TEXT_PLAYER_TWO_POINTS ENDP

	; ===========================================================================
	; PROCEDIMIENTO: DIBUJAR PANTALLA DE GAME OVER (MODIFICADO)
	; Ahora muestra opciones de reiniciar o salir y retorna AL con la tecla elegida
	; ===========================================================================
	DRAW_GAME_OVER_SCREEN PROC NEAR
		
		CALL CLEAR_SCREEN
		; MOSTRAR EL TITULO DEL MENU
		MOV AH, 02h
		MOV BH, 00h
		MOV DH, 04h
		MOV DL, 04h
		INT 10h	

		MOV AH, 09h
		LEA DX, TEXT_GAME_OVER_TITLE
		INT 21h

		; MOSTRAR EL GANADOR
		MOV AH, 02h
		MOV BH, 00h
		MOV DH, 06h
		MOV DL, 04h
		INT 10h	

		CALL UPDATE_WINNER_TEXT

		MOV AH, 09h
		LEA DX, TEXT_GAME_OVER_WINNER
		INT 21h

		; =======================================================================
		; [NUEVO] MOSTRAR OPCIONES: JUGAR DE NUEVO O SALIR
		; =======================================================================
		MOV AH, 02h
		MOV BH, 00h
		MOV DH, 09h					; FILA 9
		MOV DL, 04h
		INT 10h
		MOV AH, 09h
		LEA DX, TEXT_PLAY_AGAIN
		INT 21h

		MOV AH, 02h
		MOV BH, 00h
		MOV DH, 0Bh					; FILA 11
		MOV DL, 04h
		INT 10h
		MOV AH, 09h
		LEA DX, TEXT_PRESS_ESC
		INT 21h
		; =======================================================================

		; ESPERAR UNA PULSACION Y RETORNARLA EN AL
		MOV AH, 00h
		INT 16h						; AL = CARACTER ASCII DE LA TECLA

		RET
	DRAW_GAME_OVER_SCREEN ENDP
	; ===========================================================================

