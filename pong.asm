CODE SEGMENT PARA 'CODE' 	; DEFINICION DEL SEGMENTO DE CODIGO

	MAIN PROC FAR 					  ; PROCEDIMIENTO PRINCIPAL DEL PROGRAMA
	ASSUME CS:CODE, DS:DATA, SS:STACK ; ESTABLECER LOS SEGMENTOS A SUS DEBIDOS REGISTROS (CS, DS, SS)
	PUSH DS 						  ; ENVIAR AL STACK EL SEGMENTO DE DATOS (REGISTRO DS)
	SUB AX, AX 						  ; LIMPIAR EL REGISTRO AX
	PUSH AX							  ; ENVIAR EL REGISTRO AX AL STACK
	MOV AX, DATA					  ; GUARDAR EL CONTENIDO DEL DATA SEGMENT EN EL REGISTRO AX
	MOV DS, AX						  ; GUARDAR EN EL REGISTRO DS EL CONTENIDO DEL REGISTRO AX
	POP AX							  ; GUARDAR EL TOP DEL STACK EN EL REGISTRO AX
	POP AX							  ; GUARDAR EL TOP DEL STACK EN EL REGISTRO AX

		MOV AH, 00h  	 ; PONER LA CONFIGURACION HACIA VIDEO MODE
		MOV AL, 13h  	 ; ELEGIR EL MODO DE VIDEO 
		INT 10h      	 ; EJECUTAR INT                   

		SHOW_MENU:                      ; ETIQUETA PARA MOSTRAR EL MENU PRINCIPAL
			CALL DRAW_MENU_SCREEN		; DIBUJAR LA PANTALLA DEL MENU

		WAIT_START_KEY:                 ; ETIQUETA PARA ESPERAR TECLA DE INICIO
			MOV AH, 00h					; LEER TECLA PRESIONADA
			INT 16h                     ; EJECUTAR INT / LEER TECLA
			CMP AL, 1Bh					; ESC -> SALIR DEL PROGRAMA
			JE EXIT_PROGRAM             ; SI ES ESC, SALIR DEL PROGRAMA
			CMP AL, 0Dh					; ENTER -> INICIAR JUEGO
			JNE WAIT_START_KEY          ; SI NO ES ENTER, SEGUIR ESPERANDO

		CALL RESET_GAME_STATE           ; REINICIAR ESTADO DEL JUEGO

		CHECK_TIME: 	                ; ETIQUETA PARA VERIFICAR EL TIEMPO
			CMP GAME_ACTIVE, 00h        ; COMPARAR SI EL JUEGO ESTA ACTIVO
			JE SHOW_GAME_OVER           ; SI NO ESTA ACTIVO, MOSTRAR GAME OVER

			MOV AH, 2Ch 	 			; OBTENER EL TIEMPO DEL SISTEMA
			INT 21h 	 	 			; CH = HORA, CL = MINUTO, DH = SEGUNDO, DL = 1/100 SEGUNDO
		
			; VERIFICAR SI EL TIEMPO ACTUAL ES IGUAL AL ANTERIOR (TIME_AUX)
			CMP DL, TIME_AUX            ; COMPARAR TIEMPO ACTUAL CON AUXILIAR
		
			JE CHECK_TIME 	 			; SI ES IGUAL COMPARA OTRA VEZ SINO REALIZAR EL RESTO DE ACCIONES
			MOV TIME_AUX, DL            ; ACTUALIZAR VARIABLE DE TIEMPO AUXILIAR
			
			CALL CLEAR_SCREEN			; LIMPIAR LA PANTALLA PARA EVITAR LA VISUALIZACION DE PIXELES ANTERIORES

			CALL MOVE_BALL				; MOVER LA PELOTA
			CALL DRAW_BALL				; DIBUJAR LA PELOTA

			CALL DRAW_CENTER_LINE		; DIBUJAR LA LINEA CENTRAL DIVISORIA

			CALL MOVE_PADDLES			; MOVER LAS PALETAS MEDIANTE LOS INPUTS DEL TECLADO
			CALL DRAW_PADDLES			; DIBUJAR LAS PALETAS

			CALL DRAW_UI                ; DIBUJAR INTERFAZ DE USUARIO (PUNTOS)

			JMP CHECK_TIME 				; LUEGO DE REALIZAR LAS ACCIONES VOLVER A REVISAR EL TIEMPO	

			SHOW_GAME_OVER:             ; ETIQUETA PARA MOSTRAR PANTALLA DE GAME OVER
				CALL DRAW_GAME_OVER_SCREEN  ; DIBUJAR PANTALLA DE GAME OVER

				; DRAW_GAME_OVER_SCREEN YA ESPERA UNA TECLA
				; AL RETORNAR, AL TIENE LA TECLA PRESIONADA
				CMP AL, 0Dh				; ENTER -> JUGAR DE NUEVO
				JE RESTART_GAME         ; SI ES ENTER, REINICIAR JUEGO
				CMP AL, 1Bh				; ESC -> SALIR
				JE EXIT_PROGRAM         ; SI ES ESC, SALIR DEL PROGRAMA
				JMP SHOW_GAME_OVER		; OTRA TECLA -> ESPERAR DE NUEVO

				RESTART_GAME:           ; ETIQUETA PARA REINICIAR EL JUEGO
					CALL RESET_GAME_STATE	; REINICIAR VARIABLES DEL JUEGO
					JMP CHECK_TIME			; VOLVER AL LOOP PRINCIPAL

		EXIT_PROGRAM:                   ; ETIQUETA PARA SALIR DEL PROGRAMA
			; RESTAURAR MODO DE VIDEO A TEXTO ANTES DE SALIR
			MOV AH, 00h                 ; PREPARAR PARA RESTAURAR MODO DE VIDEO
			MOV AL, 03h                 ; SELECCIONAR MODO TEXTO
			INT 10h                     ; EJECUTAR INT / CAMBIAR MODO DE VIDEO
			MOV AH, 4Ch                 ; PREPARAR PARA FINALIZAR PROGRAMA
			INT 21h                     ; EJECUTAR INT / SALIR DEL PROGRAMA

		RET 							; RETORNAR AL SISTEMA OPERATIVO
	MAIN ENDP 						    ; FIN DEL PROCEDIMIENTO PRINCIPAL

	DRAW_MENU_SCREEN PROC NEAR          ; PROCEDIMIENTO PARA DIBUJAR EL MENU PRINCIPAL
		CALL CLEAR_SCREEN				; LIMPIAR PANTALLA

		; TITULO "PONG" - CENTRADO
		MOV AH, 02h                     ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                     ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 03h						; FILA 3
		MOV DL, 13h						; COLUMNA 19 (CENTRADO EN 40 COLS)
		INT 10h                         ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                     ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_MENU_TITLE         ; CARGAR DIRECCION DEL TITULO
		INT 21h                         ; EJECUTAR INT / MOSTRAR CADENA

		; LINEA SEPARADORA DE GUIONES (MANUAL PIXEL A PIXEL CON TEXTO)
		; CONTROLES JUGADOR 1
		MOV AH, 02h                     ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                     ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 06h						; FILA 6
		MOV DL, 0Dh						; COLUMNA 13
		INT 10h                         ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                     ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_MENU_P1            ; CARGAR DIRECCION DEL TEXTO CONTROLES P1
		INT 21h                         ; EJECUTAR INT / MOSTRAR CADENA

		; CONTROLES JUGADOR 2
		MOV AH, 02h                     ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                     ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 08h						; FILA 8
		MOV DL, 0Bh						; COLUMNA 11
		INT 10h                         ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                     ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_MENU_P2            ; CARGAR DIRECCION DEL TEXTO CONTROLES P2
		INT 21h                         ; EJECUTAR INT / MOSTRAR CADENA

		; CONDICION DE VICTORIA
		MOV AH, 02h                     ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                     ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 0Bh						; FILA 11
		MOV DL, 09h						; COLUMNA 9
		INT 10h                         ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                     ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_MENU_WIN           ; CARGAR DIRECCION DEL TEXTO CONDICION VICTORIA
		INT 21h                         ; EJECUTAR INT / MOSTRAR CADENA

		; MENSAJE DE INICIO
		MOV AH, 02h                     ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                     ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 0Eh						; FILA 14
		MOV DL, 0Ah						; COLUMNA 10
		INT 10h                         ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                     ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_MENU_START         ; CARGAR DIRECCION DEL MENSAJE DE INICIO
		INT 21h                         ; EJECUTAR INT / MOSTRAR CADENA

		RET                             ; RETORNAR DEL PROCEDIMIENTO
	DRAW_MENU_SCREEN ENDP               ; FIN DEL PROCEDIMIENTO
	
	RESET_GAME_STATE PROC NEAR          ; PROCEDIMIENTO PARA REINICIAR ESTADO DEL JUEGO
		; RESETEAR PUNTOS
		MOV PLAYER_ONE_POINTS, 00h      ; PONER PUNTOS JUGADOR 1 A 0
		MOV PLAYER_TWO_POINTS, 00h      ; PONER PUNTOS JUGADOR 2 A 0
		CALL UPDATE_TEXT_PLAYER_ONE_POINTS  ; ACTUALIZAR TEXTO PUNTOS J1
		CALL UPDATE_TEXT_PLAYER_TWO_POINTS  ; ACTUALIZAR TEXTO PUNTOS J2

		; RESETEAR POSICIONES DE PALETAS
		MOV PADDLE_LEFT_Y, 0Ah          ; RESTAURAR POSICION Y PALETA IZQUIERDA
		MOV PADDLE_RIGHT_Y, 0Ah         ; RESTAURAR POSICION Y PALETA DERECHA

		; RESETEAR PELOTA
		CALL RESET_BALL_POSITION        ; RESTAURAR POSICION DE LA PELOTA

		; RESETEAR VELOCIDADES DE LA PELOTA A LOS VALORES BASE
		MOV AX, BALL_BASE_VEL_X         ; CARGAR VELOCIDAD BASE EN X
		MOV BALL_VELOCITY_X, AX         ; RESTAURAR VELOCIDAD X
		MOV AX, BALL_BASE_VEL_Y         ; CARGAR VELOCIDAD BASE EN Y
		MOV BALL_VELOCITY_Y, AX         ; RESTAURAR VELOCIDAD Y

		; RESETEAR CONTADOR DE REBOTES
		MOV BOUNCE_COUNT, 0000h         ; REINICIAR CONTADOR DE REBOTES

		; RESETEAR INDICE DEL GANADOR
		MOV WINNER_INDEX, 00h           ; LIMPIAR INDICE DEL GANADOR

		; ACTIVAR EL JUEGO
		MOV GAME_ACTIVE, 01h            ; ESTABLECER JUEGO COMO ACTIVO

		RET                             ; RETORNAR DEL PROCEDIMIENTO
	RESET_GAME_STATE ENDP               ; FIN DEL PROCEDIMIENTO
	DRAW_CENTER_LINE PROC NEAR          ; PROCEDIMIENTO PARA DIBUJAR LINEA CENTRAL
		MOV DX, 08h						; EMPEZAR DESDE Y=8 (DEJAR BORDE SUPERIOR)
		MOV CX, 0A0h					; X = 160 (CENTRO HORIZONTAL)

		DRAW_CENTER_LOOP:               ; ETIQUETA DEL BUCLE PARA DIBUJAR PUNTEADO
			; DIBUJAR UN SEGMENTO DE 4 PIXELES DE ALTO
			MOV AH, 0Ch                 ; PREPARAR PARA DIBUJAR PIXEL
			MOV AL, 07h					; COLOR GRIS OSCURO (NO TAN BRILLANTE COMO LAS PALETAS)
			MOV BH, 00h                 ; SELECCIONAR PAGINA DE VIDEO
			INT 10h                     ; EJECUTAR INT / DIBUJAR PIXEL
			INC DX                      ; AVANZAR A SIGUIENTE FILA
			MOV AH, 0Ch                 ; PREPARAR PARA DIBUJAR PIXEL
			MOV AL, 07h                 ; COLOR GRIS OSCURO
			MOV BH, 00h                 ; SELECCIONAR PAGINA DE VIDEO
			INT 10h                     ; EJECUTAR INT / DIBUJAR PIXEL
			INC DX                      ; AVANZAR A SIGUIENTE FILA
			MOV AH, 0Ch                 ; PREPARAR PARA DIBUJAR PIXEL
			MOV AL, 07h                 ; COLOR GRIS OSCURO
			MOV BH, 00h                 ; SELECCIONAR PAGINA DE VIDEO
			INT 10h                     ; EJECUTAR INT / DIBUJAR PIXEL
			INC DX                      ; AVANZAR A SIGUIENTE FILA
			MOV AH, 0Ch                 ; PREPARAR PARA DIBUJAR PIXEL
			MOV AL, 07h                 ; COLOR GRIS OSCURO
			MOV BH, 00h                 ; SELECCIONAR PAGINA DE VIDEO
			INT 10h                     ; EJECUTAR INT / DIBUJAR PIXEL

			ADD DX, 05h					; SALTAR 5 PIXELES (ESPACIO ENTRE SEGMENTOS -> EFECTO PUNTEADO)

			MOV AX, WINDOW_HEIGHT   ; CARGAR ALTURA DE VENTANA
			SUB AX, 08h				; DEJAR BORDE INFERIOR
			CMP DX, AX              ; COMPARAR POSICION Y CON LIMITE INFERIOR
			JL DRAW_CENTER_LOOP		; CONTINUAR SI NO LLEGAMOS AL BORDE

		RET							; RETORNAR DEL PROCEDIMIENTO
	DRAW_CENTER_LINE ENDP           ; FIN DEL PROCEDIMIENTO

	DRAW_BALL PROC NEAR             ; PROCEDIMIENTO PARA DIBUJAR LA PELOTA
		
		MOV CX, BALL_X 				; SELECCIONAR CORDENADA X (COLUMNA) INICIAL
		MOV DX, BALL_Y 				; SELECCIONAR COORDENADA Y (FILA) INICIAL 
	
		DRAW_BALL_LABEL: 			; ETIQUETA PARA DIBUJAR LA PELOTA PIXEL POR PIXEL

			MOV AH, 0Ch 			; DIBUJAR UN PIXEL EN MODO GRAFICO
			MOV AL, 0Fh 			; PONER DE COLOR BLANCO AL PIXEL
			MOV BH, 00h 			; SELECCIONAR PAGINA DE VIDEO
			INT 10h 				; EJECUTAR INT
			INC CX 					; CX = CX + 1
			
			; CX - BALL_X > BALL_SIZE (Y -> IR A LA SIGUIENTE LINEA, N -> SEGUIR A LA SIGUIENTE COLUMNA)
			MOV AX, CX              ; COPIAR POSICION X ACTUAL A AX
			SUB AX, BALL_X          ; RESTAR POSICION INICIAL X
			CMP AX, BALL_SIZE       ; COMPARAR CON TAMAÑO DE PELOTA
			
			JNG DRAW_BALL_LABEL 	; SALTAR SI NO ES MAYOR (SEGUIR EN MISMA FILA)

			MOV CX, BALL_X 			; PONER EL REGISTRO CX EN LA COLUMNA INICIAL
			INC DX					; AVANZAR UNA LINEA
			
			; DX - BALL_Y > BALL_SIZE (Y -> TERMINAR PROCEDIMIENTO, N -> SEGUIR A LA SIGUIENTE LINEA)
			MOV AX, DX              ; COPIAR POSICION Y ACTUAL A AX
			SUB AX, BALL_Y          ; RESTAR POSICION INICIAL Y
			CMP AX, BALL_SIZE       ; COMPARAR CON TAMAÑO DE PELOTA
			JNG DRAW_BALL_LABEL     ; SALTAR SI NO ES MAYOR (SEGUIR DIBUJANDO)

		RET 									; RETORNAR DEL PROCEDIMIENTO
	DRAW_BALL ENDP 								; FIN DEL PROCEDIMIENTO PARA DIBUJAR LA PELOTA

	DRAW_PADDLES PROC NEAR 						; PROCEDIMIENTO PARA DIBUJAR LAS PALETAS
		MOV CX, PADDLE_LEFT_X 					; SELECCIONAR CORDENADA X (COLUMNA) INICIAL
		MOV DX, PADDLE_LEFT_Y 					; SELECCIONAR COORDENADA Y (FILA) INICIAL 

		DRAW_PADDLE_LEFT_HORIZONTAL: 			; ETIQUETA PARA DIBUJAR LA PALETA IZQUIERDA
			MOV AH, 0Ch 						; DIBUJAR UN PIXEL EN MODO GRAFICO
			MOV AL, 0Fh 						; PONER DE COLOR BLANCO AL PIXEL
			MOV BH, 00h 						; SELECCIONAR PAGINA DE VIDEO
			INT 10h 							; EJECUTAR INT
			
			INC CX 								; CX = CX + 1
			; CX - PADDLE_LEFT_X > PADDLE_WIDTH (Y -> IR A LA SIGUIENTE LINEA, N -> SEGUIR A LA SIGUIENTE COLUMNA)
			MOV AX, CX                          ; COPIAR POSICION X ACTUAL A AX
			SUB AX, PADDLE_LEFT_X               ; RESTAR POSICION INICIAL X
			CMP AX, PADDLE_WIDTH                ; COMPARAR CON ANCHO DE PALETA
			
			JNG DRAW_PADDLE_LEFT_HORIZONTAL 	; SALTAR SI NO ES MAYOR (SEGUIR EN MISMA FILA)

			MOV CX, PADDLE_LEFT_X 				; PONER EL REGISTRO CX EN LA COLUMNA INICIAL
			INC DX								; AVANZAR UNA LINEA
			
			; DX - PADDLE_LEFT_Y > PADDLE_HEIGHT (Y -> TERMINAR PROCEDIMIENTO, N -> SEGUIR A LA SIGUIENTE LINEA)
			MOV AX, DX                          ; COPIAR POSICION Y ACTUAL A AX
			SUB AX, PADDLE_LEFT_Y               ; RESTAR POSICION INICIAL Y
			CMP AX, PADDLE_HEIGHT               ; COMPARAR CON ALTO DE PALETA
			JNG DRAW_PADDLE_LEFT_HORIZONTAL     ; SALTAR SI NO ES MAYOR (SEGUIR DIBUJANDO)

		MOV CX, PADDLE_RIGHT_X 					; SELECCIONAR CORDENADA X (COLUMNA) INICIAL
		MOV DX, PADDLE_RIGHT_Y 					; SELECCIONAR COORDENADA Y (FILA) INICIAL 

		DRAW_PADDLE_RIGHT_HORIZONTAL: 			; ETIQUETA PARA DIBUJAR LA PALETA DERECHA
			MOV AH, 0Ch 						; DIBUJAR UN PIXEL EN MODO GRAFICO
			MOV AL, 0Fh 						; PONER DE COLOR BLANCO AL PIXEL
			MOV BH, 00h 						; SELECCIONAR PAGINA DE VIDEO
			INT 10h 							; EJECUTAR INT
			
			INC CX 								; CX = CX + 1
			; CX - PADDLE_RIGHT_X > PADDLE_WIDTH (Y -> IR A LA SIGUIENTE LINEA, N -> SEGUIR A LA SIGUIENTE COLUMNA)
			MOV AX, CX                          ; COPIAR POSICION X ACTUAL A AX
			SUB AX, PADDLE_RIGHT_X              ; RESTAR POSICION INICIAL X
			CMP AX, PADDLE_WIDTH                ; COMPARAR CON ANCHO DE PALETA
			
			JNG DRAW_PADDLE_RIGHT_HORIZONTAL 	; SALTAR SI NO ES MAYOR (SEGUIR EN MISMA FILA)

			MOV CX, PADDLE_RIGHT_X 				; PONER EL REGISTRO CX EN LA COLUMNA INICIAL
			INC DX								; AVANZAR UNA LINEA
			
			; DX - PADDLE_RIGHT_Y > PADDLE_HEIGHT (Y -> TERMINAR PROCEDIMIENTO, N -> SEGUIR A LA SIGUIENTE LINEA)
			MOV AX, DX                          ; COPIAR POSICION Y ACTUAL A AX
			SUB AX, PADDLE_RIGHT_Y              ; RESTAR POSICION INICIAL Y
			CMP AX, PADDLE_HEIGHT               ; COMPARAR CON ALTO DE PALETA
			JNG DRAW_PADDLE_RIGHT_HORIZONTAL    ; SALTAR SI NO ES MAYOR (SEGUIR DIBUJANDO)

		RET 									; RETORNAR DEL PROCEDIMIENTO
	DRAW_PADDLES ENDP 							; FIN DEL PROCEDIMIENTO PARA DIBUJAR LAS PALETAS

	MOVE_BALL PROC NEAR 						; PROCEDIMIENTO PARA MOVER LA PELOTA

		MOV AX, BALL_VELOCITY_X 		; MOVER LA PELOTA HORIZONTALMENTE
		ADD BALL_X, AX                  ; SUMAR VELOCIDAD X A POSICION X
		
		MOV AX, WINDOW_BOUNDS           ; CARGAR LIMITE DE VENTANA
		CMP BALL_X, AX	 				; BALL_X < WINDOW_BOUNDS (Y -> COLISIONO)
		JG CONTINUE_X1                  ; SI ES MAYOR, SEGUIR VERIFICANDO
		JMP GIVE_POINT_TO_PLAYER_TWO 	; SUMAR UN PUNTO AL JUGADOR DOS Y RESETAR LA PELOTA			
		CONTINUE_X1:                    ; ETIQUETA DE CONTINUACION X1

		MOV AX, WINDOW_WIDTH            ; CARGAR ANCHO DE VENTANA
		SUB AX, BALL_SIZE               ; RESTAR TAMAÑO DE PELOTA
		SUB AX, WINDOW_BOUNDS           ; RESTAR LIMITE DE VENTANA
		CMP BALL_X, AX					; BALL_X > WINDOW_WIDTH - BALL_SIZE - WINDOW_BOUNDS (Y -> COLISIONO)
		JL CONTINUE_X2                  ; SI ES MENOR, SEGUIR VERIFICANDO
		JMP GIVE_POINT_TO_PLAYER_ONE 	; SUMAR UN PUNTO AL JUGADOR UNO Y RESETAR LA PELOTA
		CONTINUE_X2:                    ; ETIQUETA DE CONTINUACION X2

		MOV AX, BALL_VELOCITY_Y			; MOVER LA PELOTA VERTICALMENTE
		ADD BALL_Y, AX                  ; SUMAR VELOCIDAD Y A POSICION Y
		
		MOV AX, WINDOW_BOUNDS           ; CARGAR LIMITE DE VENTANA
		CMP BALL_Y, AX					; BALL_Y < WINDOW_BOUNDS (Y -> COLISIONO)
		JG CONTINUE_Y1                  ; SI ES MAYOR, SEGUIR VERIFICANDO
		JMP NEG_VELOCITY_Y              ; INVERTIR VELOCIDAD EN Y
		CONTINUE_Y1:                    ; ETIQUETA DE CONTINUACION Y1

		MOV AX, WINDOW_HEIGHT           ; CARGAR ALTURA DE VENTANA
		SUB AX, BALL_SIZE               ; RESTAR TAMAÑO DE PELOTA
		SUB AX, WINDOW_BOUNDS           ; RESTAR LIMITE DE VENTANA
		CMP BALL_Y, AX                  ; COMPARAR POSICION Y CON LIMITE INFERIOR
		JL CONTINUE_Y2					; BALL_Y > WINDOW_HEIGHT - BALL_SIZE - WINDOW_BOUNDS (Y -> COLISIONO)
		JMP NEG_VELOCITY_Y              ; INVERTIR VELOCIDAD EN Y
		CONTINUE_Y2:                    ; ETIQUETA DE CONTINUACION Y2

		; VERIFICAR COLISION CON PALETA DERECHA
		MOV AX, BALL_X                  ; CARGAR POSICION X DE PELOTA
		ADD AX, BALL_SIZE               ; SUMAR TAMAÑO DE PELOTA
		CMP AX, PADDLE_RIGHT_X          ; COMPARAR CON POSICION X DE PALETA DERECHA
		JNG CHECK_COLLISION_WITH_PADDLE_LEFT   ; SI NO HAY COLISION, VERIFICAR PALETA IZQUIERDA
		
		MOV AX, PADDLE_RIGHT_X          ; CARGAR POSICION X DE PALETA DERECHA
		ADD AX, PADDLE_WIDTH            ; SUMAR ANCHO DE PALETA
		CMP BALL_X, AX                  ; COMPARAR CON POSICION X DE PELOTA
		JNL CHECK_COLLISION_WITH_PADDLE_LEFT   ; SI NO HAY COLISION, VERIFICAR PALETA IZQUIERDA

		MOV AX, BALL_Y                  ; CARGAR POSICION Y DE PELOTA
		ADD AX, BALL_SIZE               ; SUMAR TAMAÑO DE PELOTA
		CMP AX, PADDLE_RIGHT_Y          ; COMPARAR CON POSICION Y DE PALETA DERECHA
		JNG CHECK_COLLISION_WITH_PADDLE_LEFT   ; SI NO HAY COLISION, VERIFICAR PALETA IZQUIERDA

		MOV AX, PADDLE_RIGHT_Y          ; CARGAR POSICION Y DE PALETA DERECHA
		ADD AX, PADDLE_HEIGHT           ; SUMAR ALTO DE PALETA
		CMP BALL_Y, AX                  ; COMPARAR CON POSICION Y DE PELOTA
		JNL CHECK_COLLISION_WITH_PADDLE_LEFT   ; SI NO HAY COLISION, VERIFICAR PALETA IZQUIERDA

		; COLISION CON PALETA DERECHA CONFIRMADA
		NEG BALL_VELOCITY_X             ; INVERTIR VELOCIDAD HORIZONTAL
		CALL INCREASE_BALL_SPEED        ; AUMENTAR VELOCIDAD PROGRESIVAMENTE
		CALL BEEP_PADDLE				; SONIDO DE REBOTE EN PALETA
		RET                             ; RETORNAR DEL PROCEDIMIENTO

		CHECK_COLLISION_WITH_PADDLE_LEFT:       ; ETIQUETA PARA VERIFICAR COLISION CON PALETA IZQUIERDA
				MOV AX, BALL_X                  ; CARGAR POSICION X DE PELOTA
				ADD AX, BALL_SIZE               ; SUMAR TAMAÑO DE PELOTA
				CMP AX, PADDLE_LEFT_X           ; COMPARAR CON POSICION X DE PALETA IZQUIERDA
				JNG MOVE_BALL_EXIT              ; SI NO HAY COLISION, SALIR
				
				MOV AX, PADDLE_LEFT_X           ; CARGAR POSICION X DE PALETA IZQUIERDA
				ADD AX, PADDLE_WIDTH            ; SUMAR ANCHO DE PALETA
				CMP BALL_X, AX                  ; COMPARAR CON POSICION X DE PELOTA
				JNL MOVE_BALL_EXIT              ; SI NO HAY COLISION, SALIR

				MOV AX, BALL_Y                  ; CARGAR POSICION Y DE PELOTA
				ADD AX, BALL_SIZE               ; SUMAR TAMAÑO DE PELOTA
				CMP AX, PADDLE_LEFT_Y           ; COMPARAR CON POSICION Y DE PALETA IZQUIERDA
				JNG MOVE_BALL_EXIT              ; SI NO HAY COLISION, SALIR

				MOV AX, PADDLE_LEFT_Y           ; CARGAR POSICION Y DE PALETA IZQUIERDA
				ADD AX, PADDLE_HEIGHT           ; SUMAR ALTO DE PALETA
				CMP BALL_Y, AX                  ; COMPARAR CON POSICION Y DE PELOTA
				JNL MOVE_BALL_EXIT              ; SI NO HAY COLISION, SALIR

			; COLISION CON PALETA IZQUIERDA CONFIRMADA
			NEG BALL_VELOCITY_X             ; INVERTIR VELOCIDAD HORIZONTAL
			CALL INCREASE_BALL_SPEED        ; AUMENTAR VELOCIDAD PROGRESIVAMENTE
			CALL BEEP_PADDLE				; SONIDO DE REBOTE EN PALETA

		MOVE_BALL_EXIT:                     	; ETIQUETA DE SALIDA DEL MOVIMIENTO
			RET 								; RETORNAR DEL PROCEDIMIENTO

		GIVE_POINT_TO_PLAYER_ONE: 				; ETIQUETA PARA RESETEAR LA POSICION DE LA PELOTA Y AÑADIR UN PUNTO AL JUGADOR UNO
			INC PLAYER_ONE_POINTS           	; INCREMENTAR PUNTOS DEL JUGADOR UNO
			CALL RESET_BALL_POSITION        	; RESTAURAR POSICION DE LA PELOTA
			CALL RESET_BALL_SPEED		    	; RESETEAR VELOCIDAD AL MARCAR PUNTO
			CALL BEEP_SCORE				    	; SONIDO DE PUNTO ANOTADO
			CALL UPDATE_TEXT_PLAYER_ONE_POINTS  ; ACTUALIZAR TEXTO DE PUNTOS J1
			CMP PLAYER_ONE_POINTS, 05h      	; COMPARAR SI LLEGO A 5 PUNTOS
			JGE GAME_OVER                   	; SI ES MAYOR O IGUAL, TERMINAR JUEGO
			RET 								; RETORNAR DESPUES DE RESETEAR POSICION
		
		GIVE_POINT_TO_PLAYER_TWO:           	; ETIQUETA PARA DAR PUNTO AL JUGADOR DOS
			INC PLAYER_TWO_POINTS           	; INCREMENTAR PUNTOS DEL JUGADOR DOS
			CALL RESET_BALL_POSITION        	; RESTAURAR POSICION DE LA PELOTA
			CALL RESET_BALL_SPEED		    	; RESETEAR VELOCIDAD AL MARCAR PUNTO
			CALL BEEP_SCORE				    	; SONIDO DE PUNTO ANOTADO
			CALL UPDATE_TEXT_PLAYER_TWO_POINTS  ; ACTUALIZAR TEXTO DE PUNTOS J2
			CMP PLAYER_TWO_POINTS, 05h      	; COMPARAR SI LLEGO A 5 PUNTOS
			JGE GAME_OVER                   	; SI ES MAYOR O IGUAL, TERMINAR JUEGO
			RET 								; RETORNAR DESPUES DE RESETEAR POSICION
		
		NEG_VELOCITY_Y: 						; ETIQUETA PARA INVERTIR LA VELOCIDAD EN Y
			NEG BALL_VELOCITY_Y             	; NEGAR VELOCIDAD VERTICAL (CAMBIAR DIRECCION)
			CALL BEEP_WALL				    	; SONIDO DE REBOTE EN PARED
			RET 								; RETORNAR DESPUES DE INVERTIR VELOCIDAD

		GAME_OVER:							; LLAMAR CUANDO SE HALLAN COMPLETADO LOS 5 PUNTOS
			CMP PLAYER_ONE_POINTS, 05h      ; COMPARAR PUNTOS DEL JUGADOR UNO
			JNL WINNER_IS_PLAYER_ONE        ; SI ES MAYOR O IGUAL, GANA JUGADOR UNO
			JMP WINNER_IS_PLAYER_TWO        ; SI NO, GANA JUGADOR DOS

			WINNER_IS_PLAYER_ONE:           ; ETIQUETA CUANDO GANA JUGADOR UNO
				MOV WINNER_INDEX, 01h       ; ESTABLECER INDICE DE GANADOR A 1
				JMP CONTINUE_GAME_OVER      ; SALTAR A CONTINUACION

			WINNER_IS_PLAYER_TWO:           ; ETIQUETA CUANDO GANA JUGADOR DOS
				MOV WINNER_INDEX, 02h       ; ESTABLECER INDICE DE GANADOR A 2
				JMP CONTINUE_GAME_OVER      ; SALTAR A CONTINUACION
			
			CONTINUE_GAME_OVER:             ; ETIQUETA DE CONTINUACION DE GAME OVER
			MOV GAME_ACTIVE, 00h            ; DESACTIVAR EL JUEGO
			RET                             ; RETORNAR


	MOVE_BALL ENDP 							; FIN DEL PROCEDIMIENTO PARA MOVER LA PELOTA

	INCREASE_BALL_SPEED PROC NEAR           ; PROCEDIMIENTO PARA AUMENTAR VELOCIDAD
		INC BOUNCE_COUNT                    ; INCREMENTAR CONTADOR DE REBOTES

		; SOLO AUMENTAR CADA 3 REBOTES
		MOV AX, BOUNCE_COUNT                ; CARGAR CONTADOR DE REBOTES
		MOV BX, 0003h                       ; DIVISOR = 3
		XOR DX, DX                          ; LIMPIAR DX PARA DIVISION
		DIV BX                              ; DIVIDIR CONTADOR ENTRE 3
		CMP DX, 0000h                       ; VERIFICAR SI RESTO ES 0
		JNE SPEED_EXIT						; SI NO ES MULTIPLO DE 3, NO AUMENTAR

		; AUMENTAR VELOCIDAD EN X (MANTENIENDO EL SIGNO)
		MOV AX, BALL_VELOCITY_X             ; CARGAR VELOCIDAD ACTUAL EN X
		OR AX, AX                           ; VERIFICAR SIGNO DE VELOCIDAD X
		JNS INC_VEL_X_POSITIVE				; SI ES POSITIVO SALTAR

		; VELOCIDAD X NEGATIVA: DECREMENTAR (HACER MAS NEGATIVA)
		CMP AX, -0Ch						; COMPARAR CON MAXIMO NEGATIVO (-12)
		JLE SKIP_INC_X						; SI YA LLEGO AL MAXIMO NEGATIVO, NO AUMENTAR
		DEC AX                              ; DECREMENTAR (HACER MAS NEGATIVO)
		MOV BALL_VELOCITY_X, AX             ; GUARDAR NUEVA VELOCIDAD X
		JMP CHECK_VEL_Y                     ; SALTAR A VERIFICAR VELOCIDAD Y

		INC_VEL_X_POSITIVE:                 ; ETIQUETA PARA VELOCIDAD X POSITIVA
			; VELOCIDAD X POSITIVA
			MOV BX, BALL_MAX_VEL_X          ; CARGAR VELOCIDAD MAXIMA EN X
			CMP AX, BX                      ; COMPARAR VELOCIDAD ACTUAL CON MAXIMA
			JGE SKIP_INC_X					; SI YA LLEGO AL MAXIMO, NO AUMENTAR
			INC AX                          ; INCREMENTAR VELOCIDAD
			MOV BALL_VELOCITY_X, AX         ; GUARDAR NUEVA VELOCIDAD X

		SKIP_INC_X:                         ; ETIQUETA PARA SALTAR INCREMENTO X

		CHECK_VEL_Y:                        ; ETIQUETA PARA VERIFICAR VELOCIDAD Y
		; AUMENTAR VELOCIDAD EN Y (MANTENIENDO EL SIGNO)
		MOV AX, BALL_VELOCITY_Y             ; CARGAR VELOCIDAD ACTUAL EN Y
		OR AX, AX                           ; VERIFICAR SIGNO DE VELOCIDAD Y
		JNS INC_VEL_Y_POSITIVE              ; SI ES POSITIVO SALTAR

		; VELOCIDAD Y NEGATIVA
		CMP AX, -07h						; MAXIMO NEGATIVO EN Y (-7)
		JLE SPEED_EXIT                      ; SI YA LLEGO AL MAXIMO, SALIR
		DEC AX                              ; DECREMENTAR (HACER MAS NEGATIVO)
		MOV BALL_VELOCITY_Y, AX             ; GUARDAR NUEVA VELOCIDAD Y
		JMP SPEED_EXIT                      ; SALTAR A SALIDA

		INC_VEL_Y_POSITIVE:                 ; ETIQUETA PARA VELOCIDAD Y POSITIVA
			MOV BX, BALL_MAX_VEL_Y          ; CARGAR VELOCIDAD MAXIMA EN Y
			CMP AX, BX                      ; COMPARAR VELOCIDAD ACTUAL CON MAXIMA
			JGE SPEED_EXIT                  ; SI YA LLEGO AL MAXIMO, SALIR
			INC AX                          ; INCREMENTAR VELOCIDAD
			MOV BALL_VELOCITY_Y, AX         ; GUARDAR NUEVA VELOCIDAD Y

		SPEED_EXIT:                         ; ETIQUETA DE SALIDA
			RET                             ; RETORNAR DEL PROCEDIMIENTO
	INCREASE_BALL_SPEED ENDP                ; FIN DEL PROCEDIMIENTO
	
	RESET_BALL_SPEED PROC NEAR              ; PROCEDIMIENTO PARA RESETEAR VELOCIDAD
		; RESTAURAR VELOCIDADES BASE (PRESERVAR SIGNO ACTUAL DE X E Y)
		MOV AX, BALL_VELOCITY_X             ; CARGAR VELOCIDAD ACTUAL EN X
		OR AX, AX                           ; VERIFICAR SIGNO
		JNS RESET_VEL_X_POS                 ; SI ES POSITIVO, SALTAR

		; VELOCIDAD X ERA NEGATIVA -> RESTAURAR BASE NEGATIVA
		MOV AX, BALL_BASE_VEL_X             ; CARGAR VELOCIDAD BASE EN X
		NEG AX                              ; NEGAR PARA HACERLA NEGATIVA
		MOV BALL_VELOCITY_X, AX             ; GUARDAR VELOCIDAD RESTAURADA
		JMP CHECK_RESET_Y                   ; SALTAR A VERIFICAR Y

		RESET_VEL_X_POS:                    ; ETIQUETA PARA VELOCIDAD X POSITIVA
			MOV AX, BALL_BASE_VEL_X         ; CARGAR VELOCIDAD BASE EN X
			MOV BALL_VELOCITY_X, AX         ; GUARDAR VELOCIDAD RESTAURADA

		CHECK_RESET_Y:                      ; ETIQUETA PARA VERIFICAR Y
		MOV AX, BALL_VELOCITY_Y             ; CARGAR VELOCIDAD ACTUAL EN Y
		OR AX, AX                           ; VERIFICAR SIGNO
		JNS RESET_VEL_Y_POS                 ; SI ES POSITIVO, SALTAR

		MOV AX, BALL_BASE_VEL_Y             ; CARGAR VELOCIDAD BASE EN Y
		NEG AX                              ; NEGAR PARA HACERLA NEGATIVA
		MOV BALL_VELOCITY_Y, AX             ; GUARDAR VELOCIDAD RESTAURADA
		JMP RESET_SPEED_EXIT                ; SALTAR A SALIDA

		RESET_VEL_Y_POS:                    ; ETIQUETA PARA VELOCIDAD Y POSITIVA
			MOV AX, BALL_BASE_VEL_Y         ; CARGAR VELOCIDAD BASE EN Y
			MOV BALL_VELOCITY_Y, AX         ; GUARDAR VELOCIDAD RESTAURADA

		RESET_SPEED_EXIT:                   ; ETIQUETA DE SALIDA
			MOV BOUNCE_COUNT, 0000h		    ; REINICIAR CONTADOR DE REBOTES
			RET                             ; RETORNAR DEL PROCEDIMIENTO
	RESET_BALL_SPEED ENDP                   ; FIN DEL PROCEDIMIENTO
	
	BEEP_PADDLE PROC NEAR                   ; PROCEDIMIENTO PARA SONIDO DE PALETA
		; FRECUENCIA ~800 Hz: DIVISOR = 1193180 / 800 = 1491 = 05D3h
		MOV AL, 0B6h				; CONFIGURAR TIMER 2: MODO 3 (CUADRADA), CANAL 2
		OUT 43h, AL                 ; ENVIAR COMANDO DE CONFIGURACION AL TIMER
		MOV AX, 05D3h				; DIVISOR PARA ~800 Hz
		OUT 42h, AL					; ENVIAR BYTE BAJO DEL DIVISOR
		MOV AL, AH                  ; CARGAR BYTE ALTO DEL DIVISOR
		OUT 42h, AL					; ENVIAR BYTE ALTO DEL DIVISOR

		IN AL, 61h					; LEER PUERTO DEL ALTAVOZ
		OR AL, 03h					; ACTIVAR BITS 0 Y 1 (ENCENDER ALTAVOZ)
		OUT 61h, AL                 ; ENVIAR DATOS AL PUERTO DEL ALTAVOZ

		; DURACION CORTA: LOOP DE ESPERA ~15ms
		MOV CX, 0C000h              	; CARGAR CONTADOR PARA DURACION DEL TONO
		BEEP_P_WAIT: LOOP BEEP_P_WAIT   ; BUCLE DE ESPERA

		IN AL, 61h                  	; LEER PUERTO DEL ALTAVOZ
		AND AL, 0FCh					; DESACTIVAR BITS 0 Y 1 (APAGAR ALTAVOZ)
		OUT 61h, AL                 	; ENVIAR DATOS AL PUERTO DEL ALTAVOZ
		RET                         	; RETORNAR DEL PROCEDIMIENTO
	BEEP_PADDLE ENDP                    ; FIN DEL PROCEDIMIENTO
	
	BEEP_WALL PROC NEAR                 ; PROCEDIMIENTO PARA SONIDO DE PARED
		; FRECUENCIA ~400 Hz: DIVISOR = 1193180 / 400 = 2982 = 0BA6h

		MOV AL, 0B6h                	; CONFIGURAR TIMER 2: MODO 3, CANAL 2
		OUT 43h, AL                 	; ENVIAR COMANDO DE CONFIGURACION AL TIMER
		MOV AX, 0BA6h					; DIVISOR PARA ~400 Hz
		OUT 42h, AL                 	; ENVIAR BYTE BAJO DEL DIVISOR
		MOV AL, AH                  	; CARGAR BYTE ALTO DEL DIVISOR
		OUT 42h, AL                 	; ENVIAR BYTE ALTO DEL DIVISOR

		IN AL, 61h                  	; LEER PUERTO DEL ALTAVOZ
		OR AL, 03h                  	; ACTIVAR BITS 0 Y 1 (ENCENDER ALTAVOZ)
		OUT 61h, AL                 	; ENVIAR DATOS AL PUERTO DEL ALTAVOZ

		MOV CX, 08000h              	; CARGAR CONTADOR PARA DURACION DEL TONO
		BEEP_W_WAIT: LOOP BEEP_W_WAIT   ; BUCLE DE ESPERA

		IN AL, 61h                  ; LEER PUERTO DEL ALTAVOZ
		AND AL, 0FCh                ; DESACTIVAR BITS 0 Y 1 (APAGAR ALTAVOZ)
		OUT 61h, AL                 ; ENVIAR DATOS AL PUERTO DEL ALTAVOZ
		RET                         ; RETORNAR DEL PROCEDIMIENTO
	BEEP_WALL ENDP                  ; FIN DEL PROCEDIMIENTO

	BEEP_SCORE PROC NEAR			; PROCEDIMIENTO PARA SONIDO DE PUNTO
		; PRIMER TONO: ~600 Hz
		MOV AL, 0B6h                ; CONFIGURAR TIMER 2: MODO 3, CANAL 2
		OUT 43h, AL                 ; ENVIAR COMANDO DE CONFIGURACION AL TIMER
		MOV AX, 07D0h				; DIVISOR PARA ~600 Hz
		OUT 42h, AL                 ; ENVIAR BYTE BAJO DEL DIVISOR
		MOV AL, AH                  ; CARGAR BYTE ALTO DEL DIVISOR
		OUT 42h, AL                 ; ENVIAR BYTE ALTO DEL DIVISOR

		IN AL, 61h                  ; LEER PUERTO DEL ALTAVOZ
		OR AL, 03h                  ; ACTIVAR BITS 0 Y 1 (ENCENDER ALTAVOZ)
		OUT 61h, AL                 ; ENVIAR DATOS AL PUERTO DEL ALTAVOZ

		MOV CX, 0FFFFh              ; CARGAR CONTADOR PARA DURACION DEL TONO
		BEEP_S1_WAIT: LOOP BEEP_S1_WAIT ; BUCLE DE ESPERA

		IN AL, 61h                  ; LEER PUERTO DEL ALTAVOZ
		AND AL, 0FCh                ; DESACTIVAR BITS 0 Y 1 (APAGAR ALTAVOZ)
		OUT 61h, AL                 ; ENVIAR DATOS AL PUERTO DEL ALTAVOZ

		; BREVE SILENCIO
		MOV CX, 4000h               ; CARGAR CONTADOR PARA SILENCIO
		BEEP_S_SIL: LOOP BEEP_S_SIL ; BUCLE DE ESPERA (SILENCIO)

		; SEGUNDO TONO: ~1000 Hz (MAS AGUDO)
		MOV AL, 0B6h                ; CONFIGURAR TIMER 2: MODO 3, CANAL 2
		OUT 43h, AL                 ; ENVIAR COMANDO DE CONFIGURACION AL TIMER
		MOV AX, 04B0h				; DIVISOR PARA ~1000 Hz
		OUT 42h, AL                 ; ENVIAR BYTE BAJO DEL DIVISOR
		MOV AL, AH                  ; CARGAR BYTE ALTO DEL DIVISOR
		OUT 42h, AL                 ; ENVIAR BYTE ALTO DEL DIVISOR

		IN AL, 61h                  ; LEER PUERTO DEL ALTAVOZ
		OR AL, 03h                  ; ACTIVAR BITS 0 Y 1 (ENCENDER ALTAVOZ)
		OUT 61h, AL                 ; ENVIAR DATOS AL PUERTO DEL ALTAVOZ

		MOV CX, 0FFFFh              ; CARGAR CONTADOR PARA DURACION DEL TONO
		BEEP_S2_WAIT: LOOP BEEP_S2_WAIT ; BUCLE DE ESPERA

		IN AL, 61h                  ; LEER PUERTO DEL ALTAVOZ
		AND AL, 0FCh                ; DESACTIVAR BITS 0 Y 1 (APAGAR ALTAVOZ)
		OUT 61h, AL                 ; ENVIAR DATOS AL PUERTO DEL ALTAVOZ
		RET                         ; RETORNAR DEL PROCEDIMIENTO
	BEEP_SCORE ENDP                     ; FIN DEL PROCEDIMIENTO

	MOVE_PADDLES PROC NEAR 				    ; PROCEDIMIENTO PARA MOVER LAS PALETAS
		MOV AH, 01h                         ; VERIFICAR SI HAY TECLA PRESIONADA
		INT 16h                             ; EJECUTAR INT / VERIFICAR TECLA
		JZ CHECK_RIGHT_PADDLE_MOVEMENT		; ZF = 1 NO HAY TECLA PRECIONADA

		MOV AH, 00h							; REVISAR QUE TECLA ESTA SIENDO PRECIONADA
		INT 16h                             ; EJECUTAR INT / LEER TECLA

		; SI ES "W" o "w" MOVER HACIA ARRIBA 
		CMP AL, 77h  						; "w"
		JE MOVE_LEFT_PADDLE_UP				; SI ES "w", MOVER PALETA IZQUIERDA ARRIBA
		CMP AL, 57h  						; "W"
		JE MOVE_LEFT_PADDLE_UP				; SI ES "W", MOVER PALETA IZQUIERDA ARRIBA
		
		; SI ES "S" o "s" MOVER HACIA ABAJO
		CMP AL, 73h  								; "s"
		JE MOVE_LEFT_PADDLE_DOWN					; SI ES "s", MOVER PALETA IZQUIERDA ABAJO
		CMP AL, 53h  								; "S"
		JE MOVE_LEFT_PADDLE_DOWN					; SI ES "S", MOVER PALETA IZQUIERDA ABAJO
		
		JMP CHECK_RIGHT_PADDLE_MOVEMENT     		; SALTAR A VERIFICAR PALETA DERECHA
		
		MOVE_LEFT_PADDLE_UP:                		; ETIQUETA PARA MOVER PALETA IZQUIERDA ARRIBA
			MOV AX, PADDLE_VELOCITY         		; CARGAR VELOCIDAD DE PALETA
			SUB PADDLE_LEFT_Y, AX           		; RESTAR VELOCIDAD A POSICION Y
			
			MOV AX, WINDOW_BOUNDS           		; CARGAR LIMITE DE VENTANA
			CMP PADDLE_LEFT_Y, AX           		; COMPARAR POSICION CON LIMITE
			JL FIX_PADDLE_LEFT_TOP_POSITION 		; SI ES MENOR, CORREGIR POSICION
			JMP CHECK_RIGHT_PADDLE_MOVEMENT 		; SALTAR A VERIFICAR PALETA DERECHA

			FIX_PADDLE_LEFT_TOP_POSITION:   		; ETIQUETA PARA CORREGIR POSICION TOPE
				MOV AX, WINDOW_BOUNDS       		; CARGAR LIMITE DE VENTANA
				MOV PADDLE_LEFT_Y, AX       		; FIJAR POSICION EN LIMITE
				JMP CHECK_RIGHT_PADDLE_MOVEMENT 	; SALTAR A VERIFICAR PALETA DERECHA
		

		MOVE_LEFT_PADDLE_DOWN:              		; ETIQUETA PARA MOVER PALETA IZQUIERDA ABAJO
			MOV AX, PADDLE_VELOCITY         		; CARGAR VELOCIDAD DE PALETA
			ADD PADDLE_LEFT_Y, AX           		; SUMAR VELOCIDAD A POSICION Y

			MOV AX, WINDOW_HEIGHT           		; CARGAR ALTURA DE VENTANA
			SUB AX, WINDOW_BOUNDS           		; RESTAR LIMITE DE VENTANA
			SUB AX, PADDLE_HEIGHT           		; RESTAR ALTO DE PALETA
			CMP PADDLE_LEFT_Y, AX           		; COMPARAR POSICION CON LIMITE INFERIOR
			JG FIX_PADDLE_LEFT_BOTTOM_POSITION  	; SI ES MAYOR, CORREGIR POSICION
			JMP CHECK_RIGHT_PADDLE_MOVEMENT 		; SALTAR A VERIFICAR PALETA DERECHA

			FIX_PADDLE_LEFT_BOTTOM_POSITION:		; ETIQUETA PARA CORREGIR POSICION BASE
				MOV PADDLE_LEFT_Y, AX       		; FIJAR POSICION EN LIMITE INFERIOR
				JMP CHECK_RIGHT_PADDLE_MOVEMENT 	; SALTAR A VERIFICAR PALETA DERECHA

		CHECK_RIGHT_PADDLE_MOVEMENT:        		; ETIQUETA PARA VERIFICAR MOVIMIENTO PALETA DERECHA
			CMP AH, 48h  							; FLECHA ARRIBA
			JE MOVE_RIGHT_PADDLE_UP					; SI ES FLECHA ARRIBA, MOVER ARRIBA
			
			CMP AH, 50h  							; FLECHA ABAJO
			JE MOVE_RIGHT_PADDLE_DOWN				; SI ES FLECHA ABAJO, MOVER ABAJO
			JMP EXIT_PADDLE_MOVEMENT        		; SALTAR A SALIDA

			MOVE_RIGHT_PADDLE_UP:           		; ETIQUETA PARA MOVER PALETA DERECHA ARRIBA
				MOV AX, PADDLE_VELOCITY     		; CARGAR VELOCIDAD DE PALETA
				SUB PADDLE_RIGHT_Y, AX      		; RESTAR VELOCIDAD A POSICION Y
				
				MOV AX, WINDOW_BOUNDS       		; CARGAR LIMITE DE VENTANA
				CMP PADDLE_RIGHT_Y, AX      		; COMPARAR POSICION CON LIMITE
				JL FIX_PADDLE_RIGHT_TOP_POSITION    ; SI ES MENOR, CORREGIR POSICION
				JMP EXIT_PADDLE_MOVEMENT    		; SALTAR A SALIDA

				FIX_PADDLE_RIGHT_TOP_POSITION:  	; ETIQUETA PARA CORREGIR POSICION TOPE
					MOV AX, WINDOW_BOUNDS   		; CARGAR LIMITE DE VENTANA
					MOV PADDLE_RIGHT_Y, AX  		; FIJAR POSICION EN LIMITE
					JMP EXIT_PADDLE_MOVEMENT    	; SALTAR A SALIDA

			MOVE_RIGHT_PADDLE_DOWN:         		; ETIQUETA PARA MOVER PALETA DERECHA ABAJO
				MOV AX, PADDLE_VELOCITY     		; CARGAR VELOCIDAD DE PALETA
				ADD PADDLE_RIGHT_Y, AX      		; SUMAR VELOCIDAD A POSICION Y

				MOV AX, WINDOW_HEIGHT       		; CARGAR ALTURA DE VENTANA
				SUB AX, WINDOW_BOUNDS       		; RESTAR LIMITE DE VENTANA
				SUB AX, PADDLE_HEIGHT       		; RESTAR ALTO DE PALETA
				CMP PADDLE_RIGHT_Y, AX      		; COMPARAR POSICION CON LIMITE INFERIOR
				JG FIX_PADDLE_RIGHT_BOTTOM_POSITION ; SI ES MAYOR, CORREGIR POSICION
				JMP EXIT_PADDLE_MOVEMENT    		; SALTAR A SALIDA

				FIX_PADDLE_RIGHT_BOTTOM_POSITION:   ; ETIQUETA PARA CORREGIR POSICION BASE
					MOV PADDLE_RIGHT_Y, AX  		; FIJAR POSICION EN LIMITE INFERIOR
					JMP EXIT_PADDLE_MOVEMENT    	; SALTAR A SALIDA

		EXIT_PADDLE_MOVEMENT:               ; ETIQUETA DE SALIDA
			RET                             ; RETORNAR DEL PROCEDIMIENTO
	MOVE_PADDLES ENDP                       ; FIN DEL PROCEDIMIENTO

	RESET_BALL_POSITION PROC NEAR           ; PROCEDIMIENTO PARA RESETEAR POSICION DE PELOTA
		MOV AX, BALL_ORIGINAL_POS_X         ; CARGAR POSICION ORIGINAL EN X
		MOV BALL_X, AX                      ; RESTAURAR POSICION X DE PELOTA

		MOV AX, BALL_ORIGINAL_POS_Y         ; CARGAR POSICION ORIGINAL EN Y
		MOV BALL_Y, AX                      ; RESTAURAR POSICION Y DE PELOTA

		RET                                 ; RETORNAR DEL PROCEDIMIENTO
	RESET_BALL_POSITION ENDP                ; FIN DEL PROCEDIMIENTO

	DRAW_UI PROC NEAR                       ; PROCEDIMIENTO PARA DIBUJAR INTERFAZ DE USUARIO

	; DIBUJAR LOS PUNTOS DEL JUGADOR UNO 
	MOV AH, 02h                             ; PREPARAR PARA POSICIONAR CURSOR
	MOV BH, 00h                             ; SELECCIONAR PAGINA DE VIDEO 0
	MOV DH, 04h                             ; FILA 4
	MOV DL, 06h                             ; COLUMNA 6
	INT 10h	                                ; EJECUTAR INT / POSICIONAR CURSOR

	MOV AH, 09h                             ; PREPARAR PARA MOSTRAR CADENA
	LEA DX, PLAYER_ONE_POINT_TEXT           ; CARGAR DIRECCION DEL TEXTO DE PUNTOS J1
	INT 21h                                 ; EJECUTAR INT / MOSTRAR CADENA

	; DIBUJAR LOS PUNTOS DEL JUGADOR DOS
	MOV AH, 02h                             ; PREPARAR PARA POSICIONAR CURSOR
	MOV BH, 00h                             ; SELECCIONAR PAGINA DE VIDEO 0
	MOV DH, 04h                             ; FILA 4
	MOV DL, 20h                             ; COLUMNA 32
	INT 10h	                                ; EJECUTAR INT / POSICIONAR CURSOR

	MOV AH, 09h                             ; PREPARAR PARA MOSTRAR CADENA
	LEA DX, PLAYER_TWO_POINT_TEXT           ; CARGAR DIRECCION DEL TEXTO DE PUNTOS J2
	INT 21h                                 ; EJECUTAR INT / MOSTRAR CADENA

	RET                                     ; RETORNAR DEL PROCEDIMIENTO
	DRAW_UI ENDP                            ; FIN DEL PROCEDIMIENTO

	UPDATE_TEXT_PLAYER_ONE_POINTS PROC NEAR ; PROCEDIMIENTO PARA ACTUALIZAR TEXTO DE PUNTOS J1
		XOR AX,AX                           ; LIMPIAR REGISTRO AX
		MOV AL, PLAYER_ONE_POINTS           ; CARGAR PUNTOS DEL JUGADOR UNO
		ADD AL, 30h                         ; CONVERTIR A CARACTER ASCII
		MOV [PLAYER_ONE_POINT_TEXT], AL     ; GUARDAR EN VARIABLE DE TEXTO
		RET                                 ; RETORNAR DEL PROCEDIMIENTO
	UPDATE_TEXT_PLAYER_ONE_POINTS ENDP      ; FIN DEL PROCEDIMIENTO

	UPDATE_TEXT_PLAYER_TWO_POINTS PROC NEAR ; PROCEDIMIENTO PARA ACTUALIZAR TEXTO DE PUNTOS J2
		XOR AX,AX                           ; LIMPIAR REGISTRO AX
		MOV AL, PLAYER_TWO_POINTS           ; CARGAR PUNTOS DEL JUGADOR DOS
		ADD AL, 30h                         ; CONVERTIR A CARACTER ASCII
		MOV [PLAYER_TWO_POINT_TEXT], AL     ; GUARDAR EN VARIABLE DE TEXTO
		RET                                 ; RETORNAR DEL PROCEDIMIENTO
	UPDATE_TEXT_PLAYER_TWO_POINTS ENDP      ; FIN DEL PROCEDIMIENTO

	DRAW_GAME_OVER_SCREEN PROC NEAR         ; PROCEDIMIENTO PARA DIBUJAR GAME OVER
		
		CALL CLEAR_SCREEN                   ; LIMPIAR PANTALLA
		; MOSTRAR EL TITULO DEL MENU
		MOV AH, 02h                         ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                         ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 04h                         ; FILA 4
		MOV DL, 04h                         ; COLUMNA 4
		INT 10h	                            ; EJECUTAR INT / POSICIONAR CURSOR

		MOV AH, 09h                         ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_GAME_OVER_TITLE        ; CARGAR DIRECCION DEL TITULO GAME OVER
		INT 21h                             ; EJECUTAR INT / MOSTRAR CADENA

		; MOSTRAR EL GANADOR
		MOV AH, 02h                         ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                         ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 06h                         ; FILA 6
		MOV DL, 04h                         ; COLUMNA 4
		INT 10h	                            ; EJECUTAR INT / POSICIONAR CURSOR

		CALL UPDATE_WINNER_TEXT             ; ACTUALIZAR TEXTO DEL GANADOR

		MOV AH, 09h                         ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_GAME_OVER_WINNER       ; CARGAR DIRECCION DEL TEXTO GANADOR
		INT 21h                             ; EJECUTAR INT / MOSTRAR CADENA

		MOV AH, 02h                         ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                         ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 09h							; FILA 9
		MOV DL, 04h                         ; COLUMNA 4
		INT 10h                             ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                         ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_PLAY_AGAIN             ; CARGAR DIRECCION DEL TEXTO JUGAR DE NUEVO
		INT 21h                             ; EJECUTAR INT / MOSTRAR CADENA

		MOV AH, 02h                         ; PREPARAR PARA POSICIONAR CURSOR
		MOV BH, 00h                         ; SELECCIONAR PAGINA DE VIDEO 0
		MOV DH, 0Bh							; FILA 11
		MOV DL, 04h                         ; COLUMNA 4
		INT 10h                             ; EJECUTAR INT / POSICIONAR CURSOR
		MOV AH, 09h                         ; PREPARAR PARA MOSTRAR CADENA
		LEA DX, TEXT_PRESS_ESC              ; CARGAR DIRECCION DEL TEXTO SALIR CON ESC
		INT 21h                             ; EJECUTAR INT / MOSTRAR CADENA

		; ESPERAR UNA PULSACION Y RETORNARLA EN AL
		MOV AH, 00h                         ; PREPARAR PARA LEER TECLA
		INT 16h						    	; AL = CARACTER ASCII DE LA TECLA

		RET                                 ; RETORNAR DEL PROCEDIMIENTO
	DRAW_GAME_OVER_SCREEN ENDP              ; FIN DEL PROCEDIMIENTO

	UPDATE_WINNER_TEXT PROC NEAR            ; PROCEDIMIENTO PARA ACTUALIZAR TEXTO DEL GANADOR
		MOV AL, WINNER_INDEX                ; CARGAR INDICE DEL GANADOR
		ADD AL, 30h                         ; CONVERTIR A CARACTER ASCII
		MOV [TEXT_GAME_OVER_WINNER + 7], AL ; GUARDAR EN POSICION DEL TEXTO
		RET                                 ; RETORNAR DEL PROCEDIMIENTO
	UPDATE_WINNER_TEXT ENDP                 ; FIN DEL PROCEDIMIENTO

	CLEAR_SCREEN PROC NEAR                  ; PROCEDIMIENTO PARA LIMPIAR LA PANTALLA
		MOV AX, 0A000h                      ; DIRECCION DE MEMORIA DE VIDEO (MODO 13h)
		MOV ES, AX                          ; GUARDAR EN REGISTRO EXTRA
		MOV DI, 0                           ; INICIO DEL BUFFER DE VIDEO
		MOV AL, 00h                         ; COLOR NEGRO PARA LIMPIAR
		MOV CX, 64000                       ; TAMAÑO DEL BUFFER (320*200)
		CLD                                 ; LIMPIAR DIRECCION (INCREMENTAR)
		REP STOSB                           ; LLENAR BUFFER CON VALOR DE AL
		RET                                 ; RETORNAR DEL PROCEDIMIENTO
	CLEAR_SCREEN ENDP                       ; FIN DEL PROCEDIMIENTO

